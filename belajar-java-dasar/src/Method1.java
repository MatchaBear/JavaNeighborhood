public class Method1 {
    public static void main(String[] args) {        // method main
                                                    // method main
        sayHelloWorld();                            // method main
                                                    // method main
    }                                               // method main

    static void sayHelloWorld() {                   // method sayHelloWorld
        System.out.println("Hello World 1");        // method sayHelloWorld
        System.out.println("Hello World 2");        // method sayHelloWorld
        System.out.println("Hello World 3");        // method sayHelloWorld
    }                                               // method sayHelloWorld
}

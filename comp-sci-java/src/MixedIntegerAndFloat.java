public class MixedIntegerAndFloat {
    public static void main(String[] args) {
        System.out.println(1.5 + 7 / 2);
        System.out.println(1 / 2 + 1 / 2);
        System.out.println("The result is: " + 1 / 2 + 1 / 2);
        System.out.println("The result is: " + (1 / 2 + 1 / 2));
        System.out.println("The result is: " + (-3 / -2 + 1 / 2));
        System.out.println("The result is: " + (3 / -2 + 1 / 2));
        System.out.println("The result is: " + (3 ^ 2));
        System.out.println("The result is: " + (1.0 / 2.0 + 1.0 / 2.0));
        System.out.println("The result is: " + (1 / 2 + 1 / 2));
        System.out.println("The result is: " + (1.0 / 2 + 1 / 2));
        System.out.println("The result is: " + (1 / 2 + 1 / 2.0));
        System.out.println("The result is: " + (5.6 + -14));
        System.out.println("The result is: " + (6.6 / 2.2 - 2));
    }
}

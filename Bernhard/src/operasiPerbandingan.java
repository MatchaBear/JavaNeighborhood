public class operasiPerbandingan {
    public static void main(String[] args) {

        int a = 100;
        int b = 100;

        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a==b);
        System.out.println(a!=b);

        // && AND -- hanya TRUE kalo kiri kanan both TRUE
        // || OR -- hanya FALSE kalo kiri kanan both FALSE

    }
}
